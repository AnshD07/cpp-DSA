#include <iostream>
using namespace std;
int main()
{

    /*

    Left Shift :--->     <<

    Rigtht Shift :--->     >>


    In left shift like multiply with 2

    Right Shift Divide by 2

    Note : In small Number okay
    */

   
    // Right shift
    cout << (17 >> 1) << endl;
    cout << (17 >> 2) << endl;


    // Left shift
    cout << (19 << 1) << endl;
    cout << (21 << 2) << endl;
}